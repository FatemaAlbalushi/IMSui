package com.InstituteManagementSystemUI.IMSui.Model;

/**
 * The Staff class represents a Staff in the Institute Management System.
 */
public class Staff {

    /**
     * The ID of the Staff.
     */
    public int StaffId;

    /**
     * The name of the Staff.
     */
    public String StaffName;

    /**
     * The email address of the Staff.
     */
    public String StaffEmail;

    /**
     * The salary of the Staff.
     */
    public double salary;

}

